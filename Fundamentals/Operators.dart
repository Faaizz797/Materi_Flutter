/// ===== ARITHMETIC OPERATORS ===== ///

void main() {
  print(7 + 2); // ==> 9
  print(7 - 2); // ==> 5
  print(7 * 2); // ==> 14
  print(7 / 2); // ==> 3.5
  print(7 ~/ 2); // ==> 3
  print(7 % 2); // ==> 1

  print(7 * 2 + 7 % 2); // ==>
}

/// ===== COMPARISON OPERATORS  ===== ///

/*
== --> equal
!= --> not equal
> --> more than
< --> less than
>= --> more than or equal to
<= --> less than or equal to
*/

/// ===== LOGIC OPERATORS  ===== ///

/*
|| --> OR ==> True if one of the operand is True
&& --> AND ==> True if both operand are True
! --> NOT ==> reverse Boolean value, "!true" means "false"
